function controlStatementExample(statement) {
    if(statement==="gm"){
        console.log("Good Morning");
    }
    else if(statement==="ga"){
        console.log("Good Afternoon");
    }
    else if(statement==="ge"){
        console.log("Good Evening");
    }
    else{
        console.log("Invalid Statement");
    }

}
controlStatementExample("gm");
controlStatementExample("ga");
controlStatementExample("ge");
controlStatementExample("gn");
